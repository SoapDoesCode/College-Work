    <h1>About Yungblud</h1>
    <p>Dominic Richard Harrison (born 5 August 1997), known professionally as Yungblud, is an English singer, songwriter and actor. In 2018, he released his debut EP Yungblud, followed by his first full-length album 21st Century Liability. In 2019, he released his second EP, The Underrated Youth, and the following year, he released his second studio album, Weird!, which peaked at the top of the UK Albums Chart and reached number 75 on the US Billboard 200. His third album, titled Yungblud like his first EP, was released in 2022 and reached number 1 on the UK Album Charts, as well as number 45 on the Billboard 200 and number 7 on the U.S. Top Rock Albums chart.</p>
    <h2>Early life</h2>
    <p>Harrison was born on 5 August 1997, in Doncaster, South Yorkshire, to Samantha and Justin Harrison. He has two younger sisters, Jemima and Isobel.

